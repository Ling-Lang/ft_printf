#include "libft.h"

int	ft_isalnum(int c)
{
	if ((c >= 060 && c <= 071))
	{
		return (1);
	}
	if ((c >= 0101 && c <= 0132) || (c >= 0141 && c <= 0172))
	{
		return (1);
	}
	return (0);
}
